		<h1>Presenter Guidelines</h1><div class="bullets" id="sc_bullets_1">
<ul>

<li><b>Relevance:</b> Abstracts (of no more than 350 words, excluding Title, Authors and their affiliations, Acknowledgement), containing findings of new studies, should be relevant to the conference topics.</li>

<li><b>Content and structure:</b> Each abstract, with a title, name(s) of author(s) with designation(s), affiliation(s), and full address, including land phone and cell phone numbers, should be structured as follows: Background/Introduction; Objective; Methods and materials; Results; Conclusion; and Acknowledgement (name of donor only). Do not add any references. </li>

<li><b>Format:</b> Abstracts must be typed in single-line spacing. Avoid using Greek letters and symbols, such as α, β, µ, χ—rather spell these out as alpha, beta, micro, chi, etc.</li>

<li><b>Specific guidelines:</b> The following specific guidelines should be adhered to while preparing an abstract:</li>
&nbsp; &nbsp; &nbsp; &nbsp; a.<b>Title:</b> Length of the Title should not exceed 100 characters. Title should be typed in Upper/lower case.<br/>
&nbsp; &nbsp; &nbsp; &nbsp; b.	<b>Affiliation:</b> Mark name(s) of Author(s) with superscript numerals and names of organizations with relevant superscript 		numerals immediately below authors' names. Name of the presenter must be bolded, and his/her email address must be mentioned.</br>
&nbsp; &nbsp; &nbsp; &nbsp; c.	<b>Background/Introduction:</b> Provide, in one or two sentence(s), information on how important and relevant the issue being addressed is and/or why the study was undertaken.<br/>
&nbsp; &nbsp; &nbsp; &nbsp;d.	<b>Objective:</b> Give a very brief description of what the study aims at (preferably in one sentence).</br> 
&nbsp; &nbsp; &nbsp; &nbsp;e.	<b>Methods and materials:</b> Cover study design, subjects, setting/location, time period, and statistical analyses. Mention if clearance was obtained from appropriate committees/offices on ethical issues for studies involving human and animal subjects. </br>
&nbsp; &nbsp; &nbsp; &nbsp;f.	<b>Results:</b> Present the results of your study in order of importance, i.e. most important finding should be reported first.</br>
&nbsp; &nbsp; &nbsp; &nbsp;g.	<b>Conclusion:</b> Conclusion should be a short summary of the results, or a statement of the policy implications that can be derived from the results. Since there is no separate section for Discussion, include relevant but concise discussion under this section.</br>
&nbsp; &nbsp; &nbsp; &nbsp;h.	<b>Acknowledgement:</b> Acknowledge the funding agency(ies) that supported the study.</br>
<li><b>Oral or poster:</b> Authors must state whether they wish their presentation to be considered for an oral presentation or for a poster presentation.
</li>
<li><b>Grouping of abstracts:</b> Author must indicate which sub-theme/section his/her abstract belongs to.</li>
<li><b>Acceptance:</b> Abstracts will either be accepted or rejected mainly based on: (a) scientific merit, (b) clarity in presentation, and (c) grammatical soundness. Authors will be notified by email whether or not their abstracts have been accepted.</li>
<li><b>Registration:</b> is compulsory for all delegates, including those whose abstracts are accepted for poster or oral presentation.</li>
<li><b>The last date for submission of abstract:</b> Abstracts must be submitted by 15 April 2013 at the latest to: cares_2013@yahoo.com</li>
</ul>





