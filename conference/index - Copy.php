		<h1>Announcement</h1>
        
<p>
The Committee of Action for Research, Extension and Services (CARES) is a registered membership-based non-government, non-political, non-profit foundation. Its goal is to facilitate and support research in the application of bio-science and business process towards improving food security and quality-of-life, with particular focus on the rural population.  CARES was founded at the confluence of a sobering realisation and its happy mitigant: 70% of our population still work the land. Today they enjoy less than 20% of the country's GDP and every day are falling further behind in terms of education, health, income and social acceptance. We cannot hope to build a stable and prosperous future for this nation by leaving them behind.  On the other hand we have a remarkable wealth of skills and knowledge to call upon, particularly in the scientific community and its global diaspora. All that is needed is a multi-disciplinary means to bring them together. <br /> <br />

CARES has created a forum where scientists both natural and social, agriculturists, and agro-entrepreneurs can interact to better understand each other's challenges and leverage each other's skills to adapt existing knowledge or commission fresh research towards their solution.   Today, CARES works with professionals ranging from internationally celebrated scientists and academics many of whom are members of Bangladesh Academy of Sciences, to hands-on laboratory and field researchers to rural farmers and to businessmen whether a village grocer or a large transnational agro-industry. <br /><br />

<b>We takes great pleasure in inviting you to the first International Conference on Biotechnology (ICB) to be held in Dhaka on 25-26 May,2013. The two-day conference will be our first international event that we hope to host regularly in the future. The theme this year is "Application of Bio-technology in addressing the development needs of Bangladesh".  Attending are several leading scientists who will present their papers at the conference and develop recommendations for a Strategic Plan of Action to face the development needs of the country in tune with its Millennium Development Goals (MDGs). In total we hope that over 25 papers will be presented (out of more than 70 submitted) by scientists working in Bhutan, Canada, China, India, Japan, Maldives, Nepal, Singapore, Sri Lanka, Thailand, USA and of course Bangladesh. </b> <br /><br />

The conference's attractions will extend to cultural events, a gala dinner and the possibility to visit places of interest in Bangladesh. There will be an opportunity for those in the ongoing stages of research to interface with colleagues and air their hypotheses and findings.  An award will be offered for the best Poster Presentation out of the nearly 30 that will be displayed at the venue. We hope you will register for <b>CARES-ICB-2013</b> and encourage your colleagues to join you. Don't miss the first of what we hope will be a regular milestone on the road to practical application of biotechnology for the common good of man. <br /><br />
<i>We look forward to welcoming you in Dhaka! </i>
</p>
